package model;

/**
 *
 * @author ra21067003
 */
public class Aluno {
    private String nome;
    private Integer ra;
    
    public Aluno(String nome, int ra) {
        this.nome = nome;
        this.ra = ra;
    }

    /**
     * @return the nome
     */
    public String getNome() {
        return nome;
    }

    /**
     * @param nome the nome to set
     */
    public void setNome(String nome) {
        this.nome = nome;
    }

    /**
     * @return the ra
     */
    public int getRa() {
        return ra;
    }

    /**
     * @param ra the ra to set
     */
    public void setRa(Integer ra) {
        this.ra = ra;
    }
}
